      S.MASP,
      TENSP
FROM
      HUYPV_SANPHAM S
WHERE
      S.MASP NOT IN(
            SELECT
                  C.MASP
            FROM
                  HUYPV_CTHD    C
                  JOIN HUYPV_HOADON H
                  ON C.SOHD = H.SOHD
            WHERE
                  H.NGHD = TO_DATE( '2006', 'YYYY')
                  AND NUOCSX = 'Trung Quoc'
      );

