      HUYPV_SANPHAM SP
      JOIN HUYPV_HOADON
      ON;

SELECT
      DISTINCT S.MASP,
      TENSP
FROM
      SANPHAM   S
      JOIN CTHD C
      ON S.MASP = C.MASP
      AND EXISTS(
            SELECT
                  *
            FROM
                  CTHD      C
                  JOIN HOADON H
                  ON C.SOHD = H.SOHD
                  AND MONTH(NGHD) = 10
                  AND YEAR(NGHD) = 2006
                  AND MAKH IN(
                        SELECT
                              H.MAKH
                        FROM
                              HOADON    H
                              INNER JOIN KHACHHANG K
                              ON H.MAKH = K.MAKH
                        WHERE
                              HOTEN = 'NGUYEN VAN A'
                  )
                  AND S.MASP = C.MASP
      );

